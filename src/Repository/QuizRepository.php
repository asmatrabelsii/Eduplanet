    public function findQuiz(int $page, Cours $cours): PaginationInterface
    {
        $data = $this->createQueryBuilder('q')
            ->orderBy('q.id', 'ASC');

        if ($cours) {
            $data = $data
                ->andWhere('q.cours = :cours')
                ->setParameter('cours', $cours);
        }

        $data = $data
            ->getQuery()
            ->getResult();

        $quiz = $this->paginatorInterface->paginate($data, $page, 1);

        return $quiz;
    }
